clear all;
close all;
clc;
W = 0 : 0.001 : 100;
Wl = [1 1.05 -.447];
r=roots(Wl);
Wu = 0.447/r;
num = [1.05 0];
den = [1 1.05 0.447];
tf = freqs(num, den, W);
phase = angle(tf);
mag = abs(tf);
subplot(2,1,1);
semilogx(W,mag);
title('Magnitude Response');
xlabel('Frequency(rad/sec)');
ylabel('Magnitude(dB)');
grid on;
subplot(2,1,2);
semilogx(W,phase);
title('Phase Response');
xlabel('Frequency(rad/sec)');
ylabel('Phase(rad)');
grid on;