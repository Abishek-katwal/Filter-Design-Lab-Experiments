clear all;
close all; 
clc;
R=1000;
C=10^-6;
W=0:50000;
mag=W./(sqrt((W).^2+(1/(R*C).^2)));
subplot(2,1,1);
semilogx(W,mag); %logscale along x-axis to visualize larger value to smaller value
title('Magnitude Response');
xlabel('Frequency(rad/sec)');
ylabel('magnitude');
grid on;
phase=(90-atan((R*W*C))).*180/pi;
subplot(2,1,2);
semilogx(W,phase);
title('Phase Response');
xlabel('Frequency(rad/sec)');
ylabel('Phase(rad)');
grid on;
